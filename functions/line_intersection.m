function[linesIntersection] = line_intersection(p1, p2, p3, p4)
% computing the intersection points between the two lines formed by the 
% points p1, p2, p3 and p4
     
    linesIntersection = [det([ det([p1; p2]), det([p1(1) 1; p2(1) 1]);... 
                         det([p3; p4]), det([p3(1) 1; p4(1) 1])])/...
                         det([ det([p1(1) 1; p2(1) 1]), det([p1(2) 1; p2(2) 1]);...
                         det([p3(1) 1; p4(1) 1]), det([p3(2) 1; p4(2) 1])]), ...
                         det([ det([p1; p2]), det([p1(2) 1; p2(2) 1]); ...
                         det([p3; p4]), det([p3(2) 1; p4(2) 1])])/...
                         det([ det([p1(1) 1; p2(1) 1]), det([p1(2) 1; p2(2) 1]);...
                         det([p3(1) 1; p4(1) 1]), det([p3(2) 1; p4(2) 1])])];
end             